import {StrictMode} from 'react';
import {createRoot} from 'react-dom/client';
import App from './App.tsx';
import './index.css';

createRoot(document.getElementById('root')!).render(
  <StrictMode>
    <App />
  </StrictMode>,
);

// Register Progressive Web App service worker for full offline support
if ('serviceWorker' in navigator) {
  window.addEventListener('load', () => {
    navigator.serviceWorker.register('/sw.js')
      .then((reg) => {
        console.log('LingoFree Service Worker registered successfully, scope:', reg.scope);
      })
      .catch((err) => {
        console.error('LingoFree Service Worker registration failed:', err);
      });
  });
}

